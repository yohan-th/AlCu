#include "../alum1.h"

void	ft_display(int *array)
{
	int i;
	int j;

	i = 0;
	while (array[i] != -1)
	{
		j = 0;
		while (j++ < array[i])
			write(1, "|", 1);
		write(1, "\n", 1);
		i++;
	}
}
